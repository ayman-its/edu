async function main() {
  // Wipe existing services & categories
  await prisma.academicService.deleteMany({});
  await prisma.academicCategory.deleteMany({});

  // Insert categories with nested services
  for (const category of data) {
    await prisma.academicCategory.create({
      data: {
        title: category.title,
        services: {
          create: category.services.map((s) => ({
            title: s.title,
            description: s.description,
          })),
        },
      },
    });
  }

  console.log("Seed completed: academic categories and services inserted.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
